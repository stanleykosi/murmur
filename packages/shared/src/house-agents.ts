/**
 * Canonical Murmur house-agent catalog shared across services.
 *
 * This module is the single source of truth for the built-in Nova, Rex, and
 * Sage personas described in Section 3.3 of the technical specification. The
 * API seed flow and the agent orchestrator both consume these definitions so
 * voice, styling, and personality data stay perfectly aligned.
 */

import type { TtsProvider } from "./types.js";

/**
 * Stable identifier values for the built-in Murmur agents.
 *
 * These IDs are configuration keys, not database primary keys. Persisted agent
 * rows continue to use UUIDs generated by PostgreSQL.
 */
export const HOUSE_AGENT_IDS = ["nova", "rex", "sage"] as const;

/**
 * Literal union of the supported built-in Murmur agents.
 */
export type HouseAgentId = (typeof HOUSE_AGENT_IDS)[number];

/**
 * Hex color string used for room-stage styling and avatar accents.
 */
type HexColor = `#${string}`;

/**
 * Shape of a canonical built-in agent definition.
 */
export interface HouseAgentDefinition {
  id: HouseAgentId;
  name: string;
  personality: string;
  voiceId: string;
  ttsProvider: TtsProvider;
  accentColor: HexColor;
  avatarUrl: string;
}

