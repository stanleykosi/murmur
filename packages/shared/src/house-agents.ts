const HOUSE_AGENT_LOOKUP = new Map(
  HOUSE_AGENTS.map((agent) => [agent.id, agent]),
);

/**
 * Returns a built-in agent definition by its stable configuration ID.
 *
 * @param agentId - Stable configuration key such as `nova`.
 * @returns The matching house-agent definition.
 * @throws {Error} When the ID does not map to a canonical built-in agent.
 */
export function getHouseAgentById(agentId: HouseAgentId): HouseAgentDefinition {
  const match = HOUSE_AGENT_LOOKUP.get(agentId);

  if (!match) {
    throw new Error(
      `Unknown house agent "${agentId}". Update packages/shared/src/house-agents.ts and re-run the command.`,
    );
  }

  return match;
}
